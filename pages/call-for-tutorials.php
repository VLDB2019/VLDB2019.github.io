<section class="call-for-tutorials">
    <h1>VLDB 2019: Call for Contributions - Tutorials</h1>

    <p>VLDB 2019 invites submissions for tutorial proposals on all topics of potential interest to the conference attendees. Tutorial proposals should cover state-of-the-art research, development, and applications in specific data management related areas, and stimulate and facilitate future work. Tutorials on interdisciplinary directions, bridging scientific research and applied communities, novel and fast-growing directions, and significant applications are highly encouraged. We encourage tutorials in areas that may be different from the usual VLDB mainstream, but still very much related to VLDB mission and objectives of managing big data. We also encourage tutorials that apply advanced Machine Learning techniques to solve data management problems. Tutorials should be targeted for a broader audience, and they must focus neither excessively, nor exclusively, on the authors' own work.</p>

    <h3>Important Dates</h3>
    <ul>
        <li>Submission deadline: 12 March 2019 (5 p.m. PST)</li>
        <li>Notification: 16 April 2019</li>
        <li>Camera-ready abstract overview due: 4 June 2019</li>
        <li>Slides availability: 12 August 2019</li>
    </ul>

    <h3>Submission Guidelines</h3>
    <p>Tutorial submissions must be submitted electronically using <a href="https://cmt3.research.microsoft.com/VLDB2019/">CMT</a>. Submissions should be formatted using the <a href="./?formatting-guidelines">PVLDB style templates</a>, with a maximum length of 4 pages, inclusive of ALL material.</p>
    <p>Proposals should include:</p>
    <ul>
        <li>Title of the tutorial</li>
        <li>Names, affiliations and email addresses of the presenters</li>
        <li>Overview of tutorial, with justification of its relevance and timeliness</li>
        <li>Target audience and assumed background</li>
        <li>Scope and structure: enough detail to provide a sense of both the scope of material to be covered and the depth to which it will be covered</li>
        <li>Intended length of the tutorial (one session of 1.5 hours, or two sessions with a total of 3 hours on the same day). If the tutorial can be of either length, please identify which material is included for each option.</li>
        <li>References: include at least 10 primary and relevant bibliographic references on the core material of the tutorial.</li>
        <li>Brief professional biographies of presenters, with a note on their background in the area of the tutorial</li>
        <li>Identification of any other venues in which all or part of the tutorial has been or will be presented, with an explanation of how the tutorial proposal for VLDB 2019 differs from these previous or forthcoming editions.</li>
        <li>Identification of topically related tutorials by other people in major data-centric research venues in recent years.</li>
    </ul>

    <h3>Accepted Tutorials</h3>
    <p>Presentation slides will be made available to VLDB 2019 participants and will be published on the VLDB website. We require the tutorial presenters to put online a webpage that provides links to all reasonably drafted slides and references before the conference starts, for publication at the conference website; an example can be found <a href="https://shangjingbo1226.github.io/2017-08-11-kdd-tutorial/">here</a>.</p>
    <p>A four-page extended abstract, with an overview including bibliographic references, will appear in PVLDB's VLDB 2019 issue. The <a href="./?formatting-guidelines">camera-ready format</a> version should be an updated version of the original submission that addresses suggestions from the tutorial chairs and possibly other reviewers.</p>
    <p>Authors must agree to standard PVLDB copyright releases for the tutorial abstract overview as well as for their slides. Authors will retain the rights to reuse all their material in any form.</p>
    <p>If the proposal is accepted, at least one of the authors listed in the proposal is expected to attend VLDB 2019 and present the tutorial.</p>

    <h3>Tutorial Chairs</h3>
    <p class="program-chairs">
