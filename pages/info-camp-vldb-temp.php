    <h3>Activity Center Details</h3>
    <p><strong>Build It Zone: </strong>Young builders can construct to their hearts’ content in this area using Lincoln Logs, Legos, Duplos, building blocks, and connectors. From skyscrapers to farms and everything in between, this is an area where we are sure to see future architects and contractors come alive!</p>
