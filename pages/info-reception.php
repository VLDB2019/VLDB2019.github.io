    <h3>Reception Details</h3>
    <p>The reception will be located in the Oschin Space Shuttle Endeavour Pavilion, home to the historic space shuttle Endeavor. The Endeavor is the fifth orbiter space shuttle built for NASA's space shuttle program. The spacecraft went on 25 missions from 1992-2011; it is now on display at the California Science Center.</p>
    <figure class="only-450">
        <img src="img/space-shuttle.jpg" />
        <figcaption><a href="https://www.flickr.com/photos/aloha75/10108336525">Photo</a> by Sam Howzit / <a href="https://creativecommons.org/licenses/by/2.0/" class="cc-lic">CC BY</a></figcaption>
    </figure>
    <p>See the Endeavor up close as you enjoy hors d'oeuvres and beverages (alcoholic and non-alcoholic). The pavilion includes videos and displays where you can learn more about this amazing spacecraft. Note: those with big appetites may want to grab dinner before the reception.</p>

    <h3>Exhibits</h3>
    <figure class="non-450">
        <img src="img/cal-sciencenter.jpg" />
        <figcaption><a href="https://www.flickr.com/photos/genevieve719/5636266679">Photo</a> by Genevieve / <a href="https://creativecommons.org/licenses/by/2.0/" class="cc-lic">CC BY</a></figcaption>
    </figure>
    <p>During the event you will also have access to the eight interactive exhibits that are part of the Ecosystems Gallery. Each exhibit explores a different ecological principle. For example, in the Forest Zone you can check out a kelp forest through a tunnel at the bottom of an 188,000-gallon tank; in the Extreme Zone you can learn about rocky shore organisms such as sea stars (and even touch a few!). Enjoy live animals and hands-on experiences throughout in the Ecosystems Gallery.</p>
    <figure class="only-450">
        <img src="img/cal-sciencenter.jpg" />
