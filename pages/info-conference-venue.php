    <p>Los Angeles is more than just a city—it's an adventure. Beaches, whale-watching, and hiking. Celebrities and colorful locals. Temperate weather. Captivating attractions, museums, and festivals. The Westin Bonaventure Hotel & Suites, Los Angeles places you in the center of all the excitement. Our downtown Los Angeles hotel spans an entire city block in the heart of the bustling business district, near the city's best sights and sounds.</p>
    <p>At 367-foot (112 m) and 35-stories tall, the <strong>Westin Bonaventure Hotel and Suites</strong> was designed by architect <a href="https://en.wikipedia.org/wiki/John_C._Portman_Jr.">John C. Portman, Jr.,</a> and constructed between 1974 and 1976. It is the largest hotel in the city and has been featured in many movies and television series over the years including: Interstellar, Strange Days, Buck Rogers in the 25th Century (as part of the city of New Chicago), Blue Thunder, It's a Living, L.A. Law, Breathless, Matlock, This Is Spinal Tap, Hit the Booty Do, Nick of Time, Midnight Madness, Showtime, Hard to Kill, The Lincoln Lawyer, Chuck, Xanadu, Moby Dick, The Fantastic Journey and was destroyed (via special effects) in Escape from LA, Epicenter, and San Andreas. Experience one of America's legendary metropolises while staying in one of our comfortable, chic hotel rooms.</p>
    <p>We're located at the epicenter of the city's financial district, less than two blocks from the Union Bank and US Bank towers, with a skyway connecting us to downtown's World Trade Center. Leisure is equally within reach: we're the closest upscale hotel to LA's famed Dodger Stadium. Also right in our vicinity is the Figueroa Corridor/University Park area, home to University of Southern California (USC). This area encompasses the Los Angeles Memorial Coliseum, Exposition Park and Shrine Auditorium, the National History Museum, California African American Museum, and the California Science Center & IMAX theater. Stop by the city's newly-built Cathedral of Our Lady of the Angels, less than a mile from the hotel.</p>
    <p>As the entertainment capital of the world, LA is one of the country's most glamorous cities—only one component of which is its thriving film industry. Among LA's foremost addresses, we are a short stroll to the Music Center, the Museum of Contemporary Art, and Frank Gehry's Walt Disney Concert Hall. We're also only eight short blocks to the Los Angeles Convention Center, LA Live, and the celebrated Staples Center, and just 20 minutes from Los Angeles International Airport (LAX).</p>

