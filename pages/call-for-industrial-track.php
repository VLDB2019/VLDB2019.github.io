    <h1>VLDB 2019: Call for Contributions - Industrial, Applications, and Experience Track</h1>

    <p>VLDB 2019 invites submissions for papers on all topics covering innovative commercial data management systems, applications and implementations, novel applications of data technologies, and experiences in applying research advances to real-world problems.</p>
    <p>In addition to choosing talks for presentation at VLDB 2019 from the submitted papers, the chairs plan to invite industrial talks for presentation at their discretion.</p>

    <h3>Important Dates</h3>
    <ul>
        <li>Submission deadline: 20 February 2019 (5 p.m. PST)</li>
        <li>Notification: 15 April 2019</li>
        <li>Camera-ready copy due: 31 May 2019</li>
    </ul>

    <h3>Topics of Interest</h3>
    <p>The topics addressed may include consumer, enterprise, and societal data management problems. Description of products or product lines must have a strong technical component, with a clear description of use cases and the technology behind it. New applications, challenging use cases, and in depth analysis of technology trends are highly welcomed. This year, we are particularly interested in experience and applications of applying data technology to areas with a strong social impact component.</p>
    <p>Specifically, topics of interest include, but are not limited to:</p>
    <ul>
        <li>High Performance Query Processing</li>
        <li>Data Preparation and Data Wrangling</li>
        <li>Data and Text Analytics</li>
        <li>Big and Streaming Data</li>
        <li>Database and Software as a Service</li>
        <li>Database Appliances and Self-Managing Systems</li>
        <li>Distributed and Cloud Computing</li>
        <li>Sensor Data and Time Series Analysis</li>
        <li>New Hardware Trends</li>
        <li>Social Networking and Graph Databases</li>
        <li>Web Data Management and Data Lakes</li>
        <li>
            Application Areas where data management technologies are being applied
            <ul>
                <li>Blockchain</li>
                <li>Journalism and Media</li>
                <li>Education</li>
                <li>Government and Nonprofits</li>
                <li>Health and Life Sciences</li>
                <li>Sharing economy</li>
                <li>Internet of Things</li>
                <li>Emerging AI technologies for self-driving car, fact recognition, etc.</li>
                <li>Finance and Commerce</li>
                <li>Networking and TeleCommunication</li>
            </ul>
        </li>
    </ul>

    <h3>Submission Guidelines</h3>
    <p>Industrial track submissions must be submitted electronically, in PDF format, using <a href="https://cmt3.research.microsoft.com/VLDB2019/">CMT</a>. Submissions must follow <a href="./?formatting-guidelines">VLDB 2019 formatting guidelines</a>, and be limited either to 6 pages (short papers) or to 12 pages (long papers), inclusive of ALL material.</p>
    <p>Please see the <a href="./?submission-guidelines">submission guidelines</a> for more information about formatting, the submission process, handling conflict of interest, and originality requirements.</p>

