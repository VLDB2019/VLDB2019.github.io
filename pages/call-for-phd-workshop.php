    <h1>VLDB 2019: Call for Contributions - PhD Workshop</h1>

    <p>VLDB PhD Workshops are a unique opportunity for graduate students to present and discuss their research work in the context of a premier international conference. The workshop provides a forum that facilitates interactions among PhD students and stimulates feedback from more experienced researchers.</p>
    <p>VLDB 2019 welcomes submissions from PhD students at any stage of their PhD work. If you are in the early stages of your studies, the submission should clearly describe the problem focused on, explain why it is important, detail why the existing solutions are not sufficient, and give an outline of the new solutions that are pursued. If you are in the middle or close to completion, the submission should be more concrete in describing your contribution, but still in the context of the doctoral work.</p>

    <h3>Important Dates</h3>
    <ul>
        <li>Submission Deadline: 15 April 2019 (5 p.m. PST)</li>
        <li>Notification of acceptance: 20 May 2019</li>
        <li>Camera-ready copy due: 10 June 2019</li>
    </ul>

    <h3>Submission Guidelines</h3>
    <p>PhD workshop submissions must be submitted electronically, in PDF format, using <a href="https://cmt3.research.microsoft.com/VLDB2019/Submission/Index">the PhD Workshop CMT</a>. Submissions must be single-author and any supervisors must be explicitly identified with "supervised by" on the paper and below the author's name.</p>
    <p>Papers must be submitted in <a href="./?formatting-guidelines">camera-ready format</a> and be limited to 4 pages, inclusive of ALL material. It is acceptable if specific portions of the thesis work have been published or submitted for publication; being limited to 4 pages, this does not constitute a duplicate submission to VLDB.</p>

    <h3>Review Process</h3>
    <p>The review and decision of acceptance will balance many factors; these include the quality of your proposal and where you are within your doctoral education program. They also include external factors, such as ensuring that, as a group, the accepted candidates exhibit a diversity of backgrounds and topics.</p>
    <p>Candidates who have a clearly developed idea, who are formally considered by their institution to be working on their dissertation, and who still have time to be influenced by participation in the PhD Workshop will receive the strongest consideration.</p>

    <h3>Camera-ready Version of Accepted Papers</h3>
    <p>Authors of accepted papers should incorporate the changes suggested by the reviewers and add the following copyright notice to the final version of the paper:</p>
    <p style="margin-left: 20px; font-style: italic">"Proceedings of the VLDB 2019 PhD Workshop, August 26th, 2019. Los Angeles, California. Copyright (C) 2019 for this paper by its authors. Copying permitted for private and academic purposes."</p>
    <p>Also, please fill out and sign the <a href="./files/VLDB_2019_PhD_Workshop_Copyright_License_Form.pdf">copyright form</a> and upload it to the CMT system together with the final version of the paper by 10 June 2019.</p>

    <h3>Further Information</h3>
    <p>We plan to publish the workshop proceedings with all accepted papers in the CEUR-WS series.</p>

