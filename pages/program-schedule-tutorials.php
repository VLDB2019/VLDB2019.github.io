            <div class="schedule-head" id="Tutorial_5"><span class="schedule-time">11:00 &ndash; 12:30</span><strong class="main-title">Tutorial 5</strong></div>
            
            <p class="schedule-item" data-id="962">
                <strong class="schedule-item-title">Machine Learning Meets Big Spatial Data</strong>
                <span class="schedule-item-people">
                    <a class="author" href="mailto:" data-uz="sabek" data-at="cs.umn.edu">Ibrahim&nbsp;Sabek</a>&nbsp;(University of Minnesota, Twin Cities) and
                    <a class="author" href="mailto:" data-uz="mokbel" data-at="umn.edu">Mohamed&nbsp;Mokbel</a>&nbsp;(Qatar Computing Research Institute)
                </span>
                <span class="schedule-item-abstract">
                    <a class="schedule-item-action"></a>
                    The proliferation in amounts of generated data has propelled the rise of scalable machine learning solutions to efficiently analyze and extract useful insights from such data. Meanwhile, spatial data has become ubiquitous, e.g., GPS data, with increasingly sheer sizes in recent years. The applications of big spatial data span a wide spectrum of interests including tracking infectious disease, climate change simulation, drug addiction, among others. Consequently, major research efforts are exerted to support efficient analysis and intelligence inside these applications by either providing spatial extensions to existing machine learning solutions or building new solutions from scratch. In this 90-minutes tutorial, we comprehensively review the state-of-the-art work in the intersection of machine learning and big spatial data. We cover existing research efforts and challenges in three major areas of machine learning, namely, data analysis, deep learning and statistical inference, as well as two advanced spatial machine learning tasks, namely, spatial features extraction and spatial sampling. We also highlight open problems and challenges for future research in this area.
                </span>

            </p>


            <div class="schedule-head" id="Tutorial_6"><span class="schedule-time">14:00 &ndash; 15:30&nbsp; &amp; &nbsp;16:00 &ndash; 17:30</span><strong class="main-title">Tutorial 6</strong></div>
            
            <p class="schedule-item" data-id="952">
                <strong class="schedule-item-title">TextCube: Automated Construction and Multidimensional Exploration</strong>
                <span class="schedule-item-people">
                    <a class="author" href="mailto:" data-uz="yumeng5" data-at="illinois.edu">Yu&nbsp;Meng</a>&nbsp;(University of Illinois at Urbana-Champaign),
                    <a class="author" href="mailto:" data-uz="jiaxinh3" data-at="illinois.edu">Jiaxin&nbsp;Huang</a>&nbsp;(University of Illinois Urbana-Champaign),
                    <a class="author" href="mailto:" data-uz="shang7" data-at="illinois.edu">Jingbo&nbsp;Shang</a>&nbsp;(University of Illinois at Urbana-Champaign), and
                    <a class="author" href="mailto:" data-uz="hanj" data-at="illinois.edu">Jiawei&nbsp;Han</a>&nbsp;(University of Illinois at Urbana-Champaign)
                </span>
                <span class="schedule-item-abstract">
                    <a class="schedule-item-action"></a>
                    Today's society is immersed in a wealth of text data, ranging from news articles, to social media, research literature, medical records, and corporate reports. A grand challenge of data science and engineering is to develop effective and scalable methods to extract structures and knowledge from massive text data to satisfy diverse applications, without extensive, corpus-specific human annotations. In this tutorial, we show that TextCube provides a critical information organization structure that will satisfy such an information need. We overview a set of recently developed data-driven methods that facilitate automated construction of TextCubes from massive, domain-specific text corpora, and show that TextCubes so constructed will enhance text exploration and analysis for various applications. We focus on new TextCube construction methods that are scalable, weakly-supervised, domain-independent, language-agnostic, and effective (i.e., generating quality TextCubes from large corpora of various domains). We will demonstrate with real datasets (including news articles, scientific publications, and product reviews) on how TextCubes can be constructed to assist multidimensional analysis of massive text corpora.
                </span>

            </p>


            <div class="schedule-head" id="Tutorial_7"><span class="schedule-time">16:00 &ndash; 17:30</span><strong class="main-title">Tutorial 7</strong></div>
            
            <p class="schedule-item" data-id="970">
                <strong class="schedule-item-title">Personal Database Security and Trusted Execution Environments: A Tutorial at the Crossroads</strong>
                <span class="schedule-item-people">
                    <a class="author" href="mailto:" data-uz="nicolas.anciaux" data-at="inria.fr">Nicolas&nbsp;Anciaux</a>&nbsp;(INRIA),
                    <a class="author" href="mailto:" data-uz="luc.bouganim" data-at="inria.fr">Luc&nbsp;Bouganim</a>&nbsp;(INRIA),
                    <a class="author" href="mailto:" data-uz="philippe.pucheral" data-at="inria.fr">Philippe&nbsp;Pucheral</a>&nbsp;(USVQ/INRIA),
                    <a class="author" href="mailto:" data-uz="iulian.sandu-popa" data-at="uvsq.fr">Iulian&nbsp;Sandu&nbsp;Popa</a>&nbsp;(INRIA & David Lab.,Univ. of Versailles Saint-Quentin, France), and
                    <a class="author" href="mailto:" data-uz="guillaume.scerri" data-at="uvsq.fr">Guillaume&nbsp;Scerri</a>&nbsp;(UVSQ)
                </span>
                <span class="schedule-item-abstract">
                    <a class="schedule-item-action"></a>
                    Smart disclosure initiatives and new regulations such as GDPR in the EU increase the interest for Personal Data Management Systems (PDMS) being provided to individuals to preserve their entire digital life. Consequently, the thorny issue of data security becomes more and more prominent, but highly differs from traditional privacy issues in outsourced corporate databases. Concurrently, the emergence of Trusted Execution Environments (TEE) changes the game in privacy-preserving data management with novel security models. This tutorial offers a global perspective of the current state of work at the confluence of these two rapidly growing areas. The goal is threefold: (1) review and categorize PDMS solutions and identify existing privacy threats and countermeasures; (2) review new security models capitalizing on TEEs and related privacy-preserving data management solutions relevant to the personal context; (3) discuss new challenges at the intersection of PDMS security and TEE-based data management.
                </span>

            </p>


