        <strong class="accepted-title">Data Lake Management: Challenges and Opportunities</strong>
        <span class="accepted-authors">
            <a href="mailto:" data-uz="fnargesian" data-at="cs.toronto.edu">Fatemeh Nargesian</a>&nbsp;(University of Toronto),
            <a href="mailto:" data-uz="ekzhu" data-at="cs.toronto.edu">Erkang Zhu</a>&nbsp;(University of Toronto),
            <a href="mailto:" data-uz="miller" data-at="northeastern.edu">Ren√©e J. Miller</a>&nbsp;(Northeastern University),
