                    <a class="author" href="mailto:" data-uz="prg" data-at="cs.toronto.edu">Patricia&nbsp;Arocena</a>&nbsp;(TD Bank Group)
                </span>
                <span class="schedule-item-abstract">
                    <a class="schedule-item-action"></a>
                    The ubiquity of data lakes has created fascinating new challenges for data management research. In this tutorial, we review the state-of-the-art in data management for data lakes. We consider how data lakes are introducing new problems including dataset discovery and how they are changing the requirements for classic problems including data extraction, data cleaning, data integration, data versioning, and meta-data management.
