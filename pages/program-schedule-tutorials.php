    <h1>VLDB 2019: Tutorials</h1>

    <p>
        <strong class="accepted-title">Speedup Your Analytics: Automatic Parameter Tuning for Databases and Big Data Systems</strong>
        <span class="accepted-authors">
            <a href="mailto:" data-uz="jiahenglu" data-at="gmail.com">Jiaheng Lu</a>&nbsp;(University of Helsinki),
            <a href="mailto:" data-uz="axinggu" data-at="gmail.com">Yuxing Chen</a>&nbsp;(University of Helsinki),
            <a href="mailto:" data-uz="herodotos.herodotou" data-at="cut.ac.cy">Herodotos Herodotou</a>&nbsp;(Cyprus University of Technology), and
            <a href="mailto:" data-uz="shivnath" data-at="cs.duke.edu">Shivnath Babu</a>&nbsp;(Duke University)
        </span>
        <span class="accepted-abstract">
            <a class="accepted-abstract-action"></a>
            Database and big data analytics systems such as Hadoop and Spark have a large number of configuration parameters that control memory distribution, I/O optimization, parallelism, and compression. Improper parameter settings can cause significant performance degradation and stability issues. However, regular users and even expert administrators struggle to understand and tune them to achieve good performance.  In this tutorial, we review existing approaches on automatic parameter tuning for databases, Hadoop, and Spark, which we classify into six categories:  rule-based, cost modeling, simulation-based, experiment-driven, machine learning, and adaptive tuning. We describe the foundations of different automatic parameter tuning algorithms and present pros and cons of each approach. We also highlight real-world applications and systems and identify research challenges for handling cloud services, resource heterogeneity, and real-time analytics.
        </span>
    </p>

    <p>
        <strong class="accepted-title">TextCube: Automated Construction and Multidimensional Exploration</strong>
        <span class="accepted-authors">
            <a href="mailto:" data-uz="yumeng5" data-at="illinois.edu">Yu Meng</a>&nbsp;(University of Illinois at Urbana-Champaign),
            <a href="mailto:" data-uz="jiaxinh3" data-at="illinois.edu">Jiaxin Huang</a>&nbsp;(University of Illinois at Urbana-Champaign),
            <a href="mailto:" data-uz="shang7" data-at="illinois.edu">Jingbo Shang</a>&nbsp;(University of Illinois at Urbana-Champaign), and
            <a href="mailto:" data-uz="hanj" data-at="illinois.edu">Jiawei Han</a>&nbsp;(University of Illinois at Urbana-Champaign)
        </span>
        <span class="accepted-abstract">
            <a class="accepted-abstract-action"></a>
            Today's society is immersed in a wealth of text data, ranging from news articles, to social media, research literature, medical records, and corporate reports. A grand challenge of data science and engineering is to develop effective and scalable methods without extensive, corpus-specific human annotation to extract structures and knowledge from massive text data to satisfy diverse applications.   In this tutorial, we show that textcube provides a critical information organization structure that will satisfy such an information need. We overview a set of recently developed data-driven methods that facilitate automated construction of textcubes from massive, domain-specific text corpora, and show that textcubes so constructed will enhance text exploration and analysis for various applications. We focus on new textcube construction methods that are scalable, weakly-supervised, domain-independent, language-agnostic, and effective (i.e., generating quality textcubes from large corpora of various domains). We will demonstrate with real datasets (including news articles, scientific publications, and product reviews) on how cubes can be constructed to assist multidimensional analysis of massive text corpora.
        </span>
    </p>

    <p>
        <strong class="accepted-title">The Ever Evolving Online Labor Market: Overview, Challenges and Opportunities</strong>
        <span class="accepted-authors">
            <a href="mailto:" data-uz="senjutib" data-at="gmail.com">Senjuti Basu Roy</a>&nbsp;(New Jersey Institute of Technology) and
            <a href="mailto:" data-uz="sihem.amer-yahia" data-at="imag.fr">Sihem Amer-Yahia</a>&nbsp;(University of Grenoble)
        </span>
        <span class="accepted-abstract">
            <a class="accepted-abstract-action"></a>
            The goal of this tutorial is to make the audience aware of various discipline-specific research activities that could be characterized to be part of online labor markets  and advocate for a unified framework that is interdisciplinary in nature and requires convergence of different research disciplines. We will discuss how such a framework could bring transformative effect on the nexus of humans, technology, and the future of work.  This 1.5 hour tutorial has three parts: we will first describe different applications that rely on free-lancing and online labor markets. Then, we will re-visit seminal and prominent works that came out from different research communities (social science, machine learning, data management, psychology and organizational research, and economics) on the topic of online labor markets, describe their approaches and summarize their impact on science, society and industry. Finally, we will outline the requirements of a unified framework that has the potential to combine the best of all these worlds and present  modeling, data management and algorithmic challenges to conceptualize such a framework.
        </span>
    </p>

    <p>
        <strong class="accepted-title">Machine Learning Meets Big Spatial Data</strong>
        <span class="accepted-authors">
            <a href="mailto:" data-uz="sabek" data-at="cs.umn.edu">Ibrahim Sabek</a>&nbsp;(University of Minnesota, Twin Cities) and
            <a href="mailto:" data-uz="mokbel" data-at="umn.edu">Mohamed Mokbel</a>&nbsp;(Qatar Computing Research Institute)
        </span>
        <span class="accepted-abstract">
            <a class="accepted-abstract-action"></a>
            The proliferation in amounts of generated data has propelled the rise of scalable machine learning solutions to efficiently analyze and extract useful insights from such data. Meanwhile, spatial data has become ubiquitous, e.g., GPS data, with increasingly sheer sizes in recent years. The applications of big spatial data span a wide spectrum of interests including tracking infectious disease, climate change simulation, drug addiction, among others. Consequently, major research efforts are exerted to support efficient analysis and intelligence inside these applications by either providing spatial extensions to existing machine learning solutions or building new solutions from scratch. In this 90-minutes tutorial, we comprehensively review the state-of-the-art work in the intersection of machine learning and big spatial data. We cover existing research efforts and challenges in three major areas of machine learning, namely, data analysis, deep learning and statistical inference, as well as two advanced spatial machine learning tasks, namely, spatial features extraction and spatial sampling. We also highlight open problems and challenges for future research in this area.
        </span>
    </p>

