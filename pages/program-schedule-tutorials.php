            <a href="mailto:" data-uz="prg" data-at="cs.toronto.edu">Patricia Arocena</a>&nbsp;(TD Canada Trust)
        </span>
        <span class="accepted-abstract">
            <a class="accepted-abstract-action"></a>
            The ubiquity of data lakes has created fascinating new challenges for data management research. In this tutorial, we re- view the state-of-the-art in data management for data lakes. We consider how data lakes are introducing new problems including dataset discovery and how they are changing the requirements for classic problems including data extraction, data cleaning, data integration, and data versioning. The tutorial will cover enterprise data lakes and data lakes that are being used to support data science. Our focus will be on the exciting new open research challenges that data lakes are inspiring.
        </span>
    </p>

    <p>
        <strong class="accepted-title">Combating Fake News: A Data Management and Mining Perspective</strong>
        <span class="accepted-authors">
            <a href="mailto:" data-uz="laks" data-at="cs.ubc.ca">Laks V.S. Lakshmanan</a>&nbsp;(The University of British Columbia),
            <a href="mailto:" data-uz="simpsonm" data-at="uvic.ca">Michael E Simpson</a>&nbsp;(University of Victoria), and
            <a href="mailto:" data-uz="sthirumuruganathan" data-at="hbku.edu.qa">Saravanan Thirumuruganathan</a>&nbsp;(QCRI)
        </span>
        <span class="accepted-abstract">
            <a class="accepted-abstract-action"></a>
            Fake news is a major threat to global democracy resulting in diminished trust in government, journalism and civil society. The public popularity of social media and social networks has caused a contagion of fake news where conspiracy theories, disinformation and extreme views flourish. Detection and mitigation of fake news is one of the fundamental problems of our times and has attracted widespread attention. While fact checking websites such as snopes, politifact and major companies such as Google, Facebook, and Twitter have taken preliminary steps towards addressing fake news, much more remains to be done. As an interdisciplinary topic, various facets of fake news have been studied by communities as diverse as machine learning, databases, journalism, political science and many more.  The objective of this tutorial is two-fold. First, we wish to familiarize the database community about the efforts by other communities on combating fake news. We provide a panoramic view of the state-of-the-art of research on various aspects including detection, propagation, mitigation, and intervention of fake news. Next, we provide a concise and intuitive summary of prior research by the database community and how it could be used to counteract fake news. The tutorial covers research from areas such as data integration, truth discovery and fusion, probabilistic databases, knowledge graphs and crowdsourcing from the lens of fake news. Effective tools for addressing fake news could only be built by leveraging the synergistic relationship between database and other research communities. We hope that our tutorial provides an impetus towards such synthesis of ideas and the creation of new ones.
        </span>
    </p>

    <p>
        <strong class="accepted-title">Personal Database Privacy and Trusted Execution Environment: A Tutorial at the Crossroads</strong>
        <span class="accepted-authors">
            <a href="mailto:" data-uz="nicolas.anciaux" data-at="inria.fr">Nicolas Anciaux</a>&nbsp;(INRIA),
            <a href="mailto:" data-uz="luc.bouganim" data-at="inria.fr">Luc Bouganim</a>&nbsp;(INRIA),
            <a href="mailto:" data-uz="philippe.pucheral" data-at="inria.fr">Philippe Pucheral</a>&nbsp;(UVSQ/INRIA),
            <a href="mailto:" data-uz="iulian.sandu-popa" data-at="uvsq.fr">Iulian Sandu Popa</a>&nbsp;(INRIA & David Lab., UVSQ), and
            <a href="mailto:" data-uz="guillaume.scerri" data-at="uvsq.fr">Guillaume Scerri</a>&nbsp;(UVSQ)
        </span>
        <span class="accepted-abstract">
            <a class="accepted-abstract-action"></a>
            Smart disclosure initiatives and new regulations such as GDPR in the EU increase the interest for Personal Data Management Systems (PDMS) being provided to individuals to preserve their entire digital life. Consequently, the thorny issue of data privacy becomes more and more prominent, but highly differs from traditional privacy issues in outsourced corporate databases. Concurrently, the emergence of Trusted Execution Environments (TEE) changes the game in privacy-preserving data management with novel security models. This tutorial offers a global perspective of the current state of work at the confluence of these two rapidly growing areas. The goal is threefold: (1) review and categorize PDMS solutions and identify existing privacy threats and countermeasures; (2) review new security models capitalizing on TEEs and related privacy-preserving data management solutions relevant to the personal context; (3) discuss new challenges at the intersection of PDMS privacy and TEE-based data management.
        </span>
    </p>




