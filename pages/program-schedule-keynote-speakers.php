                <img class="schedule-photo" src="./img/people/MTamerOzsu.jpg">
                <strong class="schedule-item-title">Graph Processing: A Panaromic View and Some Open Problems</strong>
                <span class="schedule-item-people">
                    <span class="author">M. Tamer Özsu</span> (University of Waterloo)
                </span>
                <a href="./files/VLDB19-keynote-1-slides.pdf" target="_blank" class="slides">Slides</a>
                <span class="schedule-item-abstract">
                    <a class="schedule-item-action"></a>
                    <strong class="schedule-part-label">Abstract:</strong>
                    Graphs are not new in database community – we have long used them to model and reason about database operations and some of our earliest systems were built on graph models. There has been a renewed interest in the last decade in using graphs model real-life phenomena in many domains. Recent studies suggest that there is interest among the user community in using graphs to model applications and data that have traditionally been considered the domain of relational systems. Despite intense research and development efforts in graph processing, these efforts are fragmented, and general purpose, scalable solutions are not yet available. In this talk, I will provide a systematic look at the field and highlight some of the open research issues.
                </span>

                <span class="schedule-item-bio">
                    <a class="schedule-item-action"></a>
                    <strong class="schedule-part-label">Bio:</strong>
                    M. Tamer Özsu is a University Professor in the Cheriton School of Computer Science at the University of Waterloo where he also holds a Cheriton Faculty Fellowship. Before Waterloo, he spent 16 years at the University of Alberta. His research is on distributed data management and the management of non-conventional data. He is a Fellow of the Royal Society of Canada, American Association for the Advancement of Science, ACM and IEEE, an elected member of Academy of Science of Turkey and a member of Sigma Xi. He is the recipient of the CS-Can/Info-Can (Canadian Computer Science Society) Lifetime Achievement Award, ACM SIGMOD Test-of-Time Award, ACM SIGMOD Contributions Award and the Ohio State University College of Engineering Distinguished Alumnus Award. He is the recipient of two best paper awards and a paper with honourable mention.
                </span>
            </p>

