<section class="program-schedule-panel">
    <h1>VLDB 2019: Panel</h1>

    <h3>Opportunities for data-management research in the era of horizontal AI/ML</h3>

    <p><strong class="panel-label">Moderator:</strong>Neoklis (Alkis) Polyzotis<span class="affiliation">, Google Research</span></p>

    <div><strong class="panel-label">Participants:</strong></div>
    <ul>
        <li>Theo Rekatsinas<span class="affiliation">, University of Wisconsin</span></li>
        <li>Ce Zhang<span class="affiliation">, ETH Zurich</span></li>
        <li>Sudeepa Roy<span class="affiliation">, Duke University</span></li>
        <li>Manasi Vertak<span class="affiliation">, Verta.AI</span></li>
        <li>Neoklis (Alkis) Polyzotis<span class="affiliation">, Google Research</span></li>
    </ul>

    <div><strong class="panel-label">Abstract:</strong></div>
    <p>AI/ML is becoming a horizontal technology: its application is expanding to more domains, and its integration touches more parts of the technology stack. Given the strong dependence of ML on data, this expansion creates a new space for applying data management techniques. At the same time, the deeper integration of ML in the technology stack provides more touch points where ML can be used in data management systems and vice versa.</p>
    <p>In this panel, we invite researchers working in this domain to discuss this emerging world and its implications on data-management research. Among other topics, the discussion will touch on the opportunities for interesting research, how we can interact with other communities, what is the core expertise we bring to the table, and how we can conduct and evaluate this research effectively within our own community. The goal of the panel is to nudge the community to appreciate the opportunities in this new world of horizontal AI/ML and to spur a discussion on how we can shape an effective research agenda.</p>
