    <h1>VLDB 2019: Travel Support</h1>

    <p>We are very happy to announce travel support opportunities for students and faculty to attend VLDB 2019 in Los Angeles. The travel grants provide financial support to students and faculty affiliated with universities with limited funding opportunities. Awards have a strong focus on universities in developing countries to attend the VLDB 2019 conference in Los Angeles, California, August 26-30, 2019.</p>
    <p>The long-term goal of the program is to encourage talented young scientists to pursue a career in database research. The VLDB Travel Support Program provides 10 grants funded by the VLDB Endowment SPEND Committee. These will allow free registration and provide an additional amount of US$1,500 toward other costs, such as travel and lodging. Recipients of the travel support are responsible for their own travel arrangements, including health insurance, if necessary.</p>
    <p>Additionally, the National Science Foundation (NSF) supports travel for US-based students (enrolled in a US university). The support will be similar in nature to the SPEND Committee grants - it will provide up to US$1,500 towards travel, lodging, and registration. Please note that successful applicants of the NSF-funded travel support are obliged to use US-flag airline carriers for traveling and will be required to deliver a short written report after the conference.</p>

    <h3>Eligibility and Selection Criteria</h3>
    <p>
        <strong><em>For VLDB SPEND Committee grants:</em></strong><br />
        Applicants must be full-time students in good standing (graduate or undergraduate), or postdoctoral fellows/researchers, or faculty members who are either
    </p>
    <ul>
        <li>affiliated with universities that do not already have considerable presence at VLDB conferences</li>
        <li style="list-style: none"><em>or</em></li>
        <li>have a paper at the conference (or associated events).</li>
    </ul>
    <p>
        <strong><em>For NSF-funded travel support:</em></strong><br />
        Applicants must be US full-time students in good standing (graduate or undergraduate) who are either
    </p>
    <ul>
        <li>affiliated with US-universities that do not already have considerable presence at VLDB conferences</li>
        <li style="list-style: none"><em>or</em></li>
        <li>have a paper at the conference (or associated events).</li>
    </ul>
    <p>Applicants for either of the offered travel support must explicitly state that they do not have any other funding to attend. Preference will be given to applicants who demonstrate keen interest in database research and are from regions that are under-represented in the database community; presenting a paper accepted at the main conference or the workshops in VLDB 2019 is welcome but not mandatory.</p>

    <h3>Application Process</h3>
    <p>Applicants should fill in the form at <a href="https://forms.gle/bNAp1ahwDmr3eR767">https://forms.gle/bNAp1ahwDmr3eR767</a> by 9 June 2019. Applicants must also have a nominator (faculty advisor for student applicants, or senior faculty member or department head for faculty applicants), who must fill in the nominator form following the link: <a href="https://forms.gle/77VGWPgsX8qSeJ348">https://forms.gle/77VGWPgsX8qSeJ348</a>.</p>
    <p>Additionally, nominators are kindly asked to send an extra email from their official email-account to <span class="hl-blue">vldbtravelgrant<span class="mail-sep">(AT)</span>gmail.com</span>, before 9 June 2019, with the subject line of the email being "VLDB 2019 Travel Support Nomination for " in which they confirm their nomination of the applicant.</p>
    <p>Should you have any problems with opening the links or filling in the forms, please contact us via <span class="hl-blue">vldbtravelgrant<span class="mail-sep">(AT)</span>gmail.com</span>. Please note: Applications of US students will be automatically assessed also for the NSF-funded travel grant. US-students do not need to follow a separate application process.</p>
    <p>Selected candidates will be informed by 1 July 2019. Persons applying for the VLDB travel support are advised to not register before hearing from us (or by 22 July 2019, if for some reason you don't hear from us by then), since the travel support includes free registration; note that the early registration deadline is 22 July 2019.</p>

