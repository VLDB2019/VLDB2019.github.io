<section class="call-for-workshops">
    <h1>VLDB 2019: Call for Contributions - Workshops</h1>

    <p>VLDB 2019 invites submissions for workshop proposals on all topics covered by the conference. Workshop proposals that advance the field of data management to new directions are specially encouraged. VLDB workshops may be half-day or full-day events.</p>
    <p>All VLDB 2019 workshops will be "serviced," meaning that logistics such as registration, catering, room allocation, and audio-visual arrangements will be handled by the VLDB conference organizers. The workshop organizers are responsible for defining the technical program and workshop format.</p>

    <h3>Important Dates</h3>
    <ul>
        <li>Submission deadline: <em class="extended-date">6 January 2019</em> 11 January 2019 (11:59 p.m. PST)</li>
        <li>Notification: <em class="extended-date">27 January 2019</em> 3 February 2019</li>
    </ul>

    <h3>Submission Guidelines</h3>
    <p>Workshop proposals should be submitted via e-mail to workshop program co-chairs Sharad Mehrotra (sharad<span class="mail-sep">(AT)</span>ics.uci.edu) and Yuanyuan Tian (ytian<span class="mail-sep">(AT)</span>us.ibm.com) with "VLDB Workshop Proposal" in the subject line. Workshop organizers with special requirements should contact the workshop program chair as soon as possible; such requests will be considered on a case-by-case basis.</p>
    <p>Proposals must be in pdf, without exceeding 8 pages, and including the following (in the order given):</p>
    <ul>
        <li>workshop title</li>
        <li>chairs and organizers</li>
        <li>contact information</li>
        <li>a description of the workshop's topic and its goals</li>
        <li>a statement on how the workshop complements or relates to other events</li>
        <li>desired workshop format, including preferred date and duration</li>
        <li>a description of the submission review process, including key dates and coverage of how conflicts of interest are handled</li>
        <li>plans for publicity</li>
        <li>plans for disseminating the results of the workshop (e.g., proceedings details)</li>
        <li>potential (or accepted) program committee members</li>
        <li>potential (or accepted) speakers or keynotes</li>
        <li>plans for seeking sponsorship, if any</li>
        <li>
            historical information about each of the most recent previous editions of the workshop (if this is not its first edition), including:
            <ul>
                <li>date, venue, and affiliated event</li>
                <li>number of submitted and accepted papers</li>
                <li>number of participants</li>
            </ul>
        </li>
    </ul>

    <h3>Additional Information</h3>
    <p>Accepted workshops will be scheduled considering the preferred date and duration, the workshop topics, and the number of available slots. We expect that VLDB 2019 will be able to accommodate up to four parallel workshops on each of the two workshop days.</p>
    <p>The organizers of accepted workshops are required to announce the workshop and call for papers, solicit submissions, conduct the review process, compose a workshop program, and organize the proceedings. Workshop programs must comply with a joint catering schedule, and workshop programs must be published 15 days prior to the early registration deadline.</p>
    <p>Proposal evaluation criteria include relevance to VLDB, scientific quality, quality of proposed organization, evidence of interest (e.g., previous submission and attendance counts), and overlap with other proposals.</p>

    <h3>Workshop Program Chairs</h3>
    <p class="program-chairs">
        Sharad Mehrotra<small>, UCI</small><br />
        Yuanyuan Tian<small>, IBM Research</small>
    </p>
</section>
